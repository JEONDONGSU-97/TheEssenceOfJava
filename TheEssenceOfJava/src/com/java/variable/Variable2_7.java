package com.java.variable;

public class Variable2_7 {

	public static void main(String[] args) {
		System.out.println(true+ "");
		System.out.println('A'+'B');	// 'A'와'B'의 문자코드의 값 65,66
		System.out.println('1'+2);		// '1'의 문자코드의 값 49
		System.out.println('1'+'2');	// '2'의 문자코드의 값 50
		System.out.println('J'+"ava");  // 문자열과 덧셈연산을 하면 그 결과는 항상 문자열이 된다.
	}

}

